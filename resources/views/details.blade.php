           // Wait for the DOM to load
document.addEventListener("DOMContentLoaded", function () {
  // Get the carousel element
  const carousel = document.getElementById("indicators-carousel");
  
  // Get the carousel items
  const items = carousel.getElementsByClassName("duration-700");

  // Initialize variables
  let currentSlide = 0;
  let intervalId;

  // Function to show the current slide
  function showSlide(slideIndex) {
    // Hide all slides
    for (let i = 0; i < items.length; i++) {
      items[i].classList.add("hidden");
    }

    // Show the current slide
    items[slideIndex].classList.remove("hidden");
  }

  // Function to start the carousel
  function startCarousel() {
    // Show the initial slide
    showSlide(currentSlide);

    // Increment the current slide index
    currentSlide++;

    // Check if we reached the end of the slides
    if (currentSlide >= items.length) {
      currentSlide = 0;
    }
  }

  // Start the carousel initially
  startCarousel();

  // Set an interval to switch slides every 3 seconds (adjust as needed)
  intervalId = setInterval(startCarousel, 3000);
});

